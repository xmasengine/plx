// ==============================================================
//  WLA-DX banking setup
// ==============================================================
// .memorymap
// defaultslot 0
// slotsize 0x8000
// slot 0 0x0000
// .endme
// .rombankmap
// bankstotal 1
// banksize 0x8000
// banks 1
// .endro

// ==============================================================
//  SDSC tag and SMS rom header
// ==============================================================
// .sdsctag 1.2,"Hello World!","SMS programming tutorial program","Maxim"

// .bank 0 slot 0

org 0x0000
// ==============================================================
//  Boot section
// ==============================================================
    di              //  disable interrupts
    im 1            //  Interrupt mode 1
    jp main         //  jump to main program

// Interrupt handler
org 0x0038
	reti // do nothing for now

org 0x0066
// ==============================================================
//  Pause button handler
// ==============================================================
    //  Do nothing
    retn

// ==============================================================
//  Main program
// ==============================================================
main:
    ld sp, 0xdff0

    // ==============================================================
    //  Set up VDP registers
    // ==============================================================
    ld hl,VdpData
    ld b,VdpDataEnd-VdpData
    ld c,0xbf
    otir

    // ==============================================================
    //  Clear VRAM
    // ==============================================================
    //  1. Set VRAM write address to 0 by outputting 0x4000 ORed with 0x0000
    ld a,0x00
    out (0xbf),a
    ld a,0x40
    out (0xbf),a
    //  2. Output 16KB of zeroes
    ld bc, 0x4000    //  Counter for 16KB of VRAM
    ClearVRAMLoop:
        ld a,0x00    //  Value to write
        out (0xbe),a //  Output to VRAM address, which is auto-incremented after each write
        dec bc
        ld a,b
        or c
        jp nz,ClearVRAMLoop

    // ==============================================================
    //  Load palette
    // ==============================================================
    //  1. Set VRAM write address to CRAM (palette) address 0 (for palette index 0)
    //  by outputting 0xc000 ORed with 0x0000
    ld a,0x00
    out (0xbf),a
    ld a,0xc0
    out (0xbf),a
    //  2. Output colour data
    ld hl,PaletteData
    ld b,PaletteDataEnd-PaletteData
    ld c,0xbe
    otir

    // ==============================================================
    //  Load tiles (font)
    // ==============================================================
    //  1. Set VRAM write address to tile index 0
    //  by outputting 0x4000 ORed with 0x0000
    ld a,0x00
    out (0xbf),a
    ld a,0x40
    out (0xbf),a
    //  2. Output tile data
    ld hl,FontData              //  Location of tile data
    ld bc,FontDataEnd-FontData  //  Counter for number of bytes to write
    WriteTilesLoop:
        //  Output data byte then three zeroes, because our tile data is 1 bit
        //  and must be increased to 4 bit
        ld a,(hl)        //  Get data byte
        out (0xbe),a
        inc hl           //  Add one to hl so it points to the next data byte
        dec bc
        ld a,b
        or c
        jp nz,WriteTilesLoop

    // ==============================================================
    //  Write text to name table
    // ==============================================================
    //  1. Set VRAM write address to name table index 0
    //  by outputting 0x4000 ORed with 0x3800+0
    ld a,0x00
    out (0xbf),a
    ld a,0x38|0x40
    out (0xbf),a
    //  2. Output tilemap data
    ld hl,Message
    ld bc,MessageEnd-Message  //  Counter for number of bytes to write
    WriteTextLoop:
        ld a,(hl)    //  Get data byte
        out (0xbe),a
        inc hl       //  Point to next letter
        dec bc
        ld a,b
        or c
        jp nz,WriteTextLoop

    //  Turn screen on
    ld a,  0b11000000
//           |||| |`- Zoomed sprites -> 16x16 pixels
//           |||| `-- Doubled sprites -> 2 tiles per sprite, 8x16
//           |||`---- 30 row/240 line mode
//           ||`----- 28 row/224 line mode
//           |`------ VBlank interrupts
//           `------- Enable display
    out (0xbf),a
    ld a,0x81
    out (0xbf),a

    //  Infinite loop to stop program
Loop:
     jp Loop

